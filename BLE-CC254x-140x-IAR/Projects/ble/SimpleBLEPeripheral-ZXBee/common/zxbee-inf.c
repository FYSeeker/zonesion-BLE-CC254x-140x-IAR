/*********************************************************************************************
* 名称：ZXBeeInfSend()
* 功能：节点发送无线数据包给汇集节点
* 参数：*p -- 要发送的无线数据包
* 返回：
* 修改：
* 注释：
*********************************************************************************************/
void ZXBeeInfSend(char *p, int len)
{
  bleSend(p, len);
}
/*********************************************************************************************
* 名称：ZXBeeInfRecv()
* 功能：节点收到无线数据包
* 参数：*pkg -- 收到的无线数据包
* 返回：
* 修改：
* 注释：
*********************************************************************************************/
void ZXBeeInfRecv(char *pkg, int len)
{
  
  char *rdat = ZXBeeDecodePackage(pkg, len);
  if (rdat != NULL) {
    ZXBeeInfSend(rdat, strlen(rdat));
  }
}

